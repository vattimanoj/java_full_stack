class AssignmentOperator
{
public static void main(String[] args)
{
int a=1;
int b=2;
System.out.println(" Add and Assign :"+(a+=b));
System.out.println(" sub and assign :"+(a-=b));
System.out.println(" div and assign :"+(a/=b));
System.out.println(" mul and assign :"+(a*=b));
System.out.println(" mod and assign :"+(a%=b));
}
}